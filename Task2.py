# Начальные данные
зарплата = 5000  # Ежемесячная зарплата
потратить = 6000  # Расходы за первый месяц
месяцы = 10  # Количество месяцев
увеличение = 0.03  # Ежемесячный рост цен

денежный_капитал = 0  # Начальный капитал
текущие_расходы = потратить  # Начальные расходы

# Рассчитываем необходимую подушку безопасности
for _ in range(месяцы):
    дефицит = текущие_расходы - зарплата  # Вычисляем дефицит
    if дефицит > 0:  # Если расходы больше зарплаты
        денежный_капитал += дефицит  # Увеличиваем капитал на дефицит
    текущие_расходы *= (1 + увеличение)  # Увеличиваем расходы на 3%

# Округляем результат
денежный_капитал = round(денежный_капитал)

# Вывод результата
print(f"Подушка безопасности, чтобы протянуть {месяцы} месяцев без долгов: {денежный_капитал}")