# Функция для поиска индекса товара
def find_item_index(items, target):
    try:
        return items.index(target)  # Возвращаем индекс целевого элемента
    except ValueError:  # Если элемент не найден
        return None


# Список продуктов
список_продуктов = ['яблоко', 'банан', 'апельсин', 'груша', 'киви', 'банан']

# Список товаров для поиска
искомые_товары = ['банан', 'груша', 'персик']

# Поиск индексов для каждого товара
for товар in искомые_товары:
    index_item = find_item_index(список_продуктов, товар)
    if index_item is not None:  # Если товар найден
        print(f"Первое вхождение товара '{товар}' имеет индекс {index_item}.")
    else:  # Если товар не найден
        print(f"Товар '{товар}' не найден в списке.")