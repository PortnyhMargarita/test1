# Указанный порядок букв
указанный_порядок = [
    'у', 'л', 'к', 'о', 'м', 'р', 'ь', 'я', 'д', 'б', 'з', 'е', 'ё', 'н', 'ы',
    'й', 'а', 'т', 'ц', 'п', 'и', 'ч', 'ю', 'в', 'с', 'х', 'г', 'ш', 'ж', 'щ'
]

# Подсчет количества букв
count_letters = количество_букв(главный)

# Вычисление частоты букв
частота_букв = вычислить_частоту(count_letters)

# Создание словаря с частотами в указанном порядке
полная_частота = {буква: частота_букв.get(буква, 0.00) for буква in указанный_порядок}

# Вывод результата
for буква in указанный_порядок:
    print(f"{буква}: {полная_частота[буква]:.2f}")